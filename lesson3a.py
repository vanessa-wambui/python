# comparison operater - to compare 2 or more statements and they usually return a boolean answer

number1 = 2
number2 = 5
print("is number1 greater than number2?", number1 > number2)
print("is number1 less than number2?", number1 < number2)
print("is number1 greter than or equal to number2", number1 >= number2)
print("is number1 less than or equal to number2", number1 <= number2)
print("is number1 equal to number2", number1 == number2)
print("is number1 no equal to number2", number1 != number2)

# logical operaters - 
# logical and -it returns true if the condition/statement evaluates to true
print((3 >1) and (7 > 6))

# logical or -it evaluates to true if one of the statements is true
print((3 >1) or (7 < 6))

# logical not - to negate a statement
print(not(90 > 70))
